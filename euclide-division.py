# La division euclidienne
#
# Dividende = Diviseur * Quotient + Reste
#
# Exemple:
# Dividende = 10
# Diviseur = 3
# 10 = 3 * Quotient + Reste
#
# résultat
# Quotient = 3
# Reste = 1
#
# Étape     Reste       Test
# 1         10 - 3 = 7  Reste >= Diviseur
# 2         7 - 3 = 4   Reste >= Diviseur
# 3         4 - 3 = 1   Reste < Diviseur
 

 